// Helper function to find news by ID
export const getNewsById = (id) => {
    return newsData.find((news) => news.id === parseInt(id));
};

// Helper function to find news by slug
export const getNewsBySlug = (slug) => {
    return newsData.find((news) => news.slug === slug);
};

// Helper function to get related news (same category, excluding current)
export const getRelatedNews = (currentId, category, limit = 3) => {
    return newsData
        .filter(
            (news) =>
                news.id !== parseInt(currentId) && news.category === category,
        )
        .slice(0, limit);
};
