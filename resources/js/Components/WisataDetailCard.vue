<script setup>
import { onMounted, ref } from 'vue';
import L from 'leaflet';
import 'leaflet/dist/leaflet.css';

// Koordinat Air Terjun Tumpak Sewu
const map = ref(null);
const mapContainer = ref(null);
const tumpakSewuCoords = [-8.23147349076322, 112.9176273466266];

// Custom marker icon
const customIcon = L.icon({
    iconUrl:
        'https://raw.githubusercontent.com/pointhi/leaflet-color-markers/master/img/marker-icon-2x-blue.png',
    iconSize: [25, 41],
    iconAnchor: [12, 41],
    popupAnchor: [1, -34],
});

onMounted(() => {
    // Inisialisasi peta
    map.value = L.map(mapContainer.value, {
        zoomControl: true,
        attributionControl: false,
    }).setView(tumpakSewuCoords, 13);

    // Tambahkan tile layer
    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        maxZoom: 19,
    }).addTo(map.value);

    // Tambahkan marker
    const marker = L.marker(tumpakSewuCoords, {
        icon: customIcon,
    }).addTo(map.value);

    // Tambahkan popup
    marker
        .bindPopup(
            `
        <div class="leaflet-popup-content">
            <h4 class="font-bold text-lg">Air Terjun Tumpak Sewu</h4>
            <p class="text-sm">Lumajang, Jawa Timur</p>
        </div>
    `,
        )
        .openPopup();

    // Tambahkan circle untuk area sekitar
    L.circle(tumpakSewuCoords, {
        color: '#3388ff',
        fillColor: '#3388ff',
        fillOpacity: 0.2,
        radius: 500,
    }).addTo(map.value);
});
</script>

